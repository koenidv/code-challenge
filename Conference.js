/**
 * Conference object constructor
 */
class Conference {
    constructor(title, notes, platform, link, foundlink, date, starttime, endtime) {
        this.id = undefined
        this.title = title
        this.notes = notes
        this.platform = platform
        this.link = link
        this.foundlink = foundlink
        this.date = date
        this.starttime = starttime
        this.endtime = endtime
    }
}
