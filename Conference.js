/**
 * Conference object constructor
 */
class Conference {
    constructor(id, title, notes, platform, link, foundlink, starttime, endtime) {
        this.id = id // Int
        this.title = title // String
        this.notes = notes // String
        this.platform = platform // String
        this.link = link // String
        this.foundlink = foundlink // String
        this.starttime = starttime // Int
        this.endtime = endtime // Int
    }
}
