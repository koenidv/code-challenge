/**
 * Conference object constructor
 */
class Conference {
    constructor(id, title, notes, platform, link, foundlink, starttime, endtime) {
        this.id = id
        this.title = title
        this.notes = notes
        this.platform = platform
        this.link = link
        this.foundlink = foundlink
        this.starttime = starttime
        this.endtime = endtime
    }
}
